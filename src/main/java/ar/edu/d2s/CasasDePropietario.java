package ar.edu.d2s;

public class CasasDePropietario {
	
	private String propietario;
	private long cantDeCasas;

	public String getPropietario() {
		return propietario;
	}

	public void setPropietario(String propietario) {
		this.propietario = propietario;
	}

	public long getCantDeCasas() {
		return cantDeCasas;
	}

	public void setCantDeCasas(long cantDeCasas) {
		this.cantDeCasas = cantDeCasas;
	}
}